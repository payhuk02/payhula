function addCoursesSection(lang: string) {
  const filePath = path.join(LOCALES_DIR, `${lang}.json`);
  
  if (!fs.existsSync(filePath)) {
    console.error(`❌ Fichier introuvable: ${filePath}`);
    return;
  }
  
  try {
    const content = fs.readFileSync(filePath, 'utf-8');
    const data = JSON.parse(content);
    
    // Vérifier si la section courses existe déjà
    if (data.courses) {
      console.log(`⚠️  ${lang.toUpperCase()}: Section 'courses' existe déjà`);
      return;
    }
    
    // Ajouter la section courses
    data.courses = coursesTranslations[lang as keyof typeof coursesTranslations];
    
    // Réécrire le fichier
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
    
    console.log(`✅ ${lang.toUpperCase()}: Section 'courses' ajoutée`);
  } catch (error: any) {
    console.error(`❌ Erreur pour ${lang}:`, error.message);
  }
}

console.log('\n=== AJOUT DE LA SECTION COURSES ===\n');

// Ajouter la section courses pour ES, DE, PT
['es', 'de', 'pt'].forEach(lang => {
  addCoursesSection(lang);
});

console.log('\n✅ Terminé !\n');

